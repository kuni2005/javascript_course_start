/* Operadores de Asignación

= Asignación Ej: x = 5;

+= Asignación compuesto de suma
Ej: x += 3; //x = x + 3

*/

let miNumero = 10;
console.log(miNumero);

miNumero = 30
console.log(miNumero);

//Operador +=
miNumero += 5;
console.log(miNumero);

//Operador -=
miNumero -= 3;
console.log(miNumero);

//Operador *=
miNumero *= 2;
console.log(miNumero);

//Operador /=
miNumero /= 4;
console.log(miNumero);

//Operador %=
miNumero %= 2;
console.log(miNumero);

miNumero = 20;
miNumero **= 2;
console.log(miNumero);