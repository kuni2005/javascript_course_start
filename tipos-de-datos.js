 /* Tipos de Datos

 Number : Almacena valores numéricos
 Ej: 10,40,-10,-20,etc

 String: Almacena cadenas de caracteres
 Ej: "Hola", "Chau", "Test"

 Boolean: Almacena un valor lógico
 Ej: True o False

 Null: Ausencia de la referencia de un objeto
 Ej: null

 Undefined: Ausencia de valor
 Ej: undefined

 Typeof: Determinar que tipo de dato esta usando
 */

 //Valor Number
 let miEntero = 10;
 console.log(miEntero);
 console.log(typeof miEntero);

 //Valor Flotante
 let miFlotante = 7.0;
 console.log(miFlotante);
 console.log(typeof miFlotante);

 //Valor String
 let miCadena = "Hola soy cadena";
 console.log(miCadena);
 console.log(typeof miCadena);

 //Valor Boolean
 let miBoolean = true;
 console.log(miBoolean);
 console.log(typeof miBoolean);
 let miBoolean2 = false;
 console.log(miBoolean2);
 console.log(typeof miBoolean2);
 //Valor Null
 let miNull = null
 console.log(miNull);
 console.log(typeof miNull);

 //Valor Undefined
 let miUndefined = undefined
 console.log(miUndefined);
 console.log(typeof miUndefined);